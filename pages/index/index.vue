		// Êñ∞Â¢ûÔºöÂ§ÑÁêÜ‰∫§ÊòìÊâÄÊï∞ÊçÆÊõ¥Êñ∞
		handleExchangeDataUpdate(result) {
			if (result.success && result.data) {
				// Â∞ÜÊé•Âè£ËøîÂõûÁöÑÊï∞ÊçÆÊ†ºÂºèÂåñ
				const formattedData = {};
				
